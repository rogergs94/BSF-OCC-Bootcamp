//Data Types
//Primitive Data Types

//Numbers - Represents numeric values (ie: 0-9)
let score = 58;
console.log("Type of score:", typeof score);
console.log('Type of score:', typeof score);

//String - Represents textual data enclosed ('' or " ")
let playerName = "Mario123"
console.log ("Type of plarName:", typeof playerName);
let number = "58";
console.log ("Type of number:", typeof number);

//Boolean - Represents a value of True or False
let isAdmin = true;
console.log ("Type of isAdmin:", typeof isAdmin);

//Null - Represents intentional absence of value
let car = null;
console.log ("The value car is:", car);
console.log ("The type of car is:", typeof car);

//Undefined - Represents unintentional absence of value
let city;
console.log ("The value of city is:", city);
console.log ("The type of city is:", typeof city);